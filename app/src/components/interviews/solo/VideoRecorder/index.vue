<template>
	<placeholder
		ratio="pt-3/4"
		class="bg-grey-400"
	>
		<video-placeholder
			v-show="!showVideo"
			:name="userName"
		/>

		<video
			v-show="showVideo"
			ref="videoElementRef"
			class="transform -scale-x-100"
			autoplay
			playsinline
			muted
		/>

		<recording-indicator
			v-if="isRecording"
		/>

		<recording-countdown
			v-if="isCountdown"
			@finish="onCountdownFinish"
		/>

		<inline-controls
			:state="state"
			@toggle-camera="handleToggleCamera"
			@toggle-microphone="handleToggleMicrophone"
		/>
	</placeholder>

	<recorder-controls
		:state="state"
		@start-recording="handleStartRecording"
		@stop-recording="handleStopRecording"
		@cancel-countdown="handelCancelCountdown"
	/>
</template>

<script>
	import { reactive, ref } from 'vue';
	import { useStore } from 'vuex';
	import { statuses, useRecordingState } from './useRecordingState';
	import { useMedia } from './useMedia';

	import VideoPlaceholder from './VideoPlaceholder';
	import RecorderControls from './RecorderControls';
	import RecordingIndicator from './RecordingIndicator';
	import RecordingCountdown from './RecordingCountdown';
	import InlineControls from './InlineControls';

	export default {
		components: {
			VideoPlaceholder,
			RecorderControls,
			RecordingIndicator,
			RecordingCountdown,
			InlineControls,
		},

		props: {
			userName: {
				type: String,
				required: true,
			},
		},

		emits: [
			'started',
			'stopped',
		],

		setup(props, { emit }) {
			const store = useStore();
			const state = reactive({
				status: statuses.READY,
			});

			const {
				videoElementRef,
				startRecording,
				stopRecording,
				playbackURL,
			} = useMedia();
		
			const {
				isReady,
				isRecording,
				isCountdown,
			} = useRecordingState(state);

			const showVideo = ref(true);

			const handleStartRecording = () => {
				state.status = statuses.COUNTDOWN;
			};

			const handleStopRecording = () => {
				stopRecording();

				store.commit('interviews/setPlaybackURL', playbackURL);
				state.status = statuses.READY;

				emit('stopped');
			};

			const handelCancelCountdown = () => {
				state.status = statuses.READY;
			};
			
			const onCountdownFinish = () => {
				state.status = statuses.RECORDING;
				startRecording();
				emit('started');
			};

			const handleToggleCamera = () => {
				showVideo.value = !showVideo.value;
			};

			const handleToggleMicrophone = () => {};

			return {
				state,
				videoElementRef,
				isReady,
				isRecording,
				isCountdown,
				handleStartRecording,
				handleStopRecording,
				handelCancelCountdown,
				onCountdownFinish,
				showVideo,
				handleToggleCamera,
				handleToggleMicrophone,
			}
		},
	};
</script>
